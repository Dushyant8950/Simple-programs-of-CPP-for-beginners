#include <iostream>
using namespace std;
int main(){
    int a, b;
    cout<<"enter a=";
    cin>>a;
    cout<<"enter b=";
    cin>>b;
    // 0 = False or 1 = True
    cout<<(a==b)<<endl;
    cout<<(a!=b)<<endl;
    cout<<(a<b)<<endl;
    cout<<(a>b)<<endl;
    cout<<(a<=b)<<endl;
    cout<<(a>=b)<<endl;
    cout<<!(a==b)<<endl;
}
